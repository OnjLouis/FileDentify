        private static List<ReportOverviewRow> BuildOverviewRows(IEnumerable<FileReport> reports)
        {
            var rows = new List<ReportOverviewRow>();
            foreach (var report in reports)
            {
                var row = new ReportOverviewRow();
                row.DisplayName = report.DisplayName ?? string.Empty;
                row.LikelyType = FindReportValue(report, "Summary", "Likely type");
                if (string.IsNullOrWhiteSpace(row.LikelyType))
                    row.LikelyType = HasSection(report, "Inspection error") ? "Inspection error" : "(not reported)";
                row.Path = FindReportValue(report, "Summary", "Path");
                if (string.IsNullOrWhiteSpace(row.Path))
                    row.Path = FindReportValue(report, "Inspection error", "Path");
                row.Extension = FindReportValue(report, "Summary", "Extension");
                row.SizeBytes = ParseSizeBytes(FindReportValue(report, "Summary", "Size"));
                row.HasError = HasSection(report, "Inspection error");
                row.IsUnknown = row.LikelyType.IndexOf("Unknown binary data", StringComparison.OrdinalIgnoreCase) >= 0;
                row.NoSignatureMatch = HasItem(report, "Signature matches", "No common signature match");
                row.ByteCounts = ParseByteCounts(FindReportValue(report, "Byte statistics", "Most common bytes"));
                row.ReadableStrings = ReadableStrings(report);
                rows.Add(row);
            }
            return rows;
        }

        private static long ParseSizeBytes(string sizeText)
        {
            if (string.IsNullOrWhiteSpace(sizeText))
                return 0;
            var match = Regex.Match(sizeText, @"\(([\d,]+) bytes\)");
            if (!match.Success)
                return 0;
            long value;
            return long.TryParse(match.Groups[1].Value.Replace(",", string.Empty), NumberStyles.Integer, CultureInfo.InvariantCulture, out value) ? value : 0;
        }

        private static Dictionary<byte, int> ParseByteCounts(string text)
        {
            var counts = new Dictionary<byte, int>();
            if (string.IsNullOrWhiteSpace(text))
                return counts;
            foreach (var line in text.Split(new[] { '\r', '\n' }, StringSplitOptions.RemoveEmptyEntries))
            {
                var match = Regex.Match(line.Trim(), @"^0x([0-9A-Fa-f]{2})\s+(\d+)$");
                if (!match.Success)
                    continue;
                byte byteValue;
                int count;
                if (byte.TryParse(match.Groups[1].Value, NumberStyles.HexNumber, CultureInfo.InvariantCulture, out byteValue) &&
                    int.TryParse(match.Groups[2].Value, NumberStyles.Integer, CultureInfo.InvariantCulture, out count))
                    counts[byteValue] = count;
            }
            return counts;
        }

        private static List<string> ReadableStrings(FileReport report)
        {
            var section = report.Sections.FirstOrDefault(s => string.Equals(s.Title, "Readable text", StringComparison.OrdinalIgnoreCase));
            if (section == null)
                return new List<string>();
            return section.Items
                .Where(i => !string.Equals(i.Title, "Scan note", StringComparison.OrdinalIgnoreCase) &&
                    !string.Equals(i.Title, "Limit", StringComparison.OrdinalIgnoreCase) &&
                    !string.Equals(i.Title, "No readable text found", StringComparison.OrdinalIgnoreCase))
                .Select(i => (i.Detail ?? i.Title ?? string.Empty).Trim())
                .Where(s => s.Length >= 4 && s.Length <= 180)
                .Distinct(StringComparer.OrdinalIgnoreCase)
                .ToList();
        }

        private static IEnumerable<CommonByteRow> CommonBytes(IEnumerable<ReportOverviewRow> rows)
        {
            var totals = new Dictionary<byte, int>();
            foreach (var row in rows)
                foreach (var pair in row.ByteCounts)
                    totals[pair.Key] = totals.ContainsKey(pair.Key) ? totals[pair.Key] + pair.Value : pair.Value;
            return totals.Select(p => new CommonByteRow { ByteValue = p.Key, Count = p.Value }).OrderByDescending(r => r.Count).ThenBy(r => r.ByteValue);
        }

        private static IEnumerable<CommonStringRow> CommonReadableStrings(IEnumerable<ReportOverviewRow> rows)
        {
            return rows
                .SelectMany(r => r.ReadableStrings.Select(s => new { r.DisplayName, Value = s }))
                .GroupBy(x => x.Value, StringComparer.OrdinalIgnoreCase)
                .Select(g => new CommonStringRow { Value = g.First().Value, FileCount = g.Select(x => x.DisplayName).Distinct(StringComparer.OrdinalIgnoreCase).Count() })
                .Where(r => r.FileCount >= 2)
                .OrderByDescending(r => r.FileCount)
                .ThenBy(r => r.Value);
        }

        private static string FormatElapsed(TimeSpan elapsed)
        {
            return elapsed.TotalSeconds < 60
                ? elapsed.TotalSeconds.ToString("0.0", CultureInfo.InvariantCulture) + " seconds"
                : elapsed.ToString(@"hh\:mm\:ss", CultureInfo.InvariantCulture);
        }

        private static string FindReportValue(FileReport report, string sectionTitle, string itemTitle)
        {
            var section = report.Sections.FirstOrDefault(s => string.Equals(s.Title, sectionTitle, StringComparison.OrdinalIgnoreCase));
            if (section == null)
                return string.Empty;
            var item = section.Items.FirstOrDefault(i => string.Equals(i.Title, itemTitle, StringComparison.OrdinalIgnoreCase));
            return item == null ? string.Empty : item.Detail ?? string.Empty;
        }

        private static bool HasSection(FileReport report, string sectionTitle)
        {
            return report.Sections.Any(s => string.Equals(s.Title, sectionTitle, StringComparison.OrdinalIgnoreCase));
        }

        private static bool HasItem(FileReport report, string sectionTitle, string itemTitle)
        {
            var section = report.Sections.FirstOrDefault(s => string.Equals(s.Title, sectionTitle, StringComparison.OrdinalIgnoreCase));
            return section != null && section.Items.Any(i => string.Equals(i.Title, itemTitle, StringComparison.OrdinalIgnoreCase));
        }

        private sealed class ReportOverviewRow
        {
            public string DisplayName;
            public string LikelyType;
            public string Path;
            public string Extension;
            public long SizeBytes;
            public bool HasError;
            public bool IsUnknown;
            public bool NoSignatureMatch;
            public Dictionary<byte, int> ByteCounts = new Dictionary<byte, int>();
            public List<string> ReadableStrings = new List<string>();
        }

        private sealed class CommonByteRow
        {
            public byte ByteValue;
            public int Count;
        }

        private sealed class CommonStringRow
        {
            public string Value;
            public int FileCount;
        }

        internal static FileReport BuildErrorReport(string path, Exception ex)
        {
            var report = new FileReport();
            report.DisplayName = Path.GetFileName(path);
            report.OriginalPath = path;
            var section = AddSection(report.Sections, "Inspection error");
            Add(section, "Path", path);
            Add(section, "Error", ex.Message);
            report.FullText = BuildReportText(report);
            return report;
        }

        private static string Html(string value)
        {
            return WebUtility.HtmlEncode(value ?? string.Empty);
        }
