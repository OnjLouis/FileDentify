        private static void AppendTextOverview(StringBuilder sb, List<FileReport> reports, TimeSpan? elapsed)
        {
            var rows = BuildOverviewRows(reports);
            sb.AppendLine("Report overview");
            sb.AppendLine("===============");
            sb.AppendLine("Files:");
            sb.AppendLine(reports.Count.ToString(CultureInfo.InvariantCulture));
            if (elapsed.HasValue)
            {
                sb.AppendLine("Generation time:");
                sb.AppendLine(FormatElapsed(elapsed.Value));
            }
            sb.AppendLine("Total reported size:");
            sb.AppendLine(FormatBytes(rows.Sum(r => r.SizeBytes)));
            sb.AppendLine("Unknown type files:");
            sb.AppendLine(rows.Count(r => r.IsUnknown).ToString(CultureInfo.InvariantCulture));
            sb.AppendLine("Inspection errors:");
            sb.AppendLine(rows.Count(r => r.HasError).ToString(CultureInfo.InvariantCulture));
            sb.AppendLine("Files without a built-in signature match:");
            sb.AppendLine(rows.Count(r => r.NoSignatureMatch).ToString(CultureInfo.InvariantCulture));

            sb.AppendLine();
            sb.AppendLine("Likely types");
            sb.AppendLine("------------");
            foreach (var group in rows.GroupBy(r => r.LikelyType).OrderByDescending(g => g.Count()).ThenBy(g => g.Key).Take(25))
                sb.AppendLine(group.Count().ToString(CultureInfo.InvariantCulture) + " - " + group.Key);

            sb.AppendLine();
            sb.AppendLine("Extensions");
            sb.AppendLine("----------");
            foreach (var group in rows.GroupBy(r => string.IsNullOrWhiteSpace(r.Extension) ? "(none)" : r.Extension).OrderByDescending(g => g.Count()).ThenBy(g => g.Key).Take(25))
                sb.AppendLine(group.Count().ToString(CultureInfo.InvariantCulture) + " - " + group.Key);

            sb.AppendLine();
            sb.AppendLine("Largest files");
            sb.AppendLine("-------------");
            foreach (var row in rows.OrderByDescending(r => r.SizeBytes).Take(15))
                sb.AppendLine(FormatBytes(row.SizeBytes) + " - " + row.DisplayName + " - " + row.Path);

            var commonBytes = CommonBytes(rows).Take(16).ToArray();
            if (commonBytes.Length > 0)
            {
                sb.AppendLine();
                sb.AppendLine("Most common sampled bytes across files");
                sb.AppendLine("--------------------------------------");
                foreach (var item in commonBytes)
                    sb.AppendLine("0x" + item.ByteValue.ToString("X2", CultureInfo.InvariantCulture) + " - " + item.Count.ToString(CultureInfo.InvariantCulture));
            }

            var commonStrings = CommonReadableStrings(rows).Take(30).ToArray();
            if (commonStrings.Length > 0)
            {
                sb.AppendLine();
                sb.AppendLine("Common readable strings");
                sb.AppendLine("-----------------------");
                foreach (var item in commonStrings)
                    sb.AppendLine(item.FileCount.ToString(CultureInfo.InvariantCulture) + " files - " + item.Value);
            }

            var attention = rows.Where(r => r.IsUnknown || r.HasError).Take(50).ToArray();
            if (attention.Length > 0)
            {
                sb.AppendLine();
                sb.AppendLine("Needs attention");
                sb.AppendLine("---------------");
                foreach (var row in attention)
                    sb.AppendLine(row.DisplayName + " - " + row.LikelyType + " - " + row.Path);
            }
        }

