            sb.AppendLine("<table>");
            sb.AppendLine("<tbody>");
            HtmlMetric(sb, "Files", reports.Count.ToString(CultureInfo.InvariantCulture));
            if (elapsed.HasValue)
                HtmlMetric(sb, "Generation time", FormatElapsed(elapsed.Value));
            HtmlMetric(sb, "Total reported size", FormatBytes(rows.Sum(r => r.SizeBytes)));
            HtmlMetric(sb, "Unknown type files", rows.Count(r => r.IsUnknown).ToString(CultureInfo.InvariantCulture));
            HtmlMetric(sb, "Inspection errors", rows.Count(r => r.HasError).ToString(CultureInfo.InvariantCulture));
            HtmlMetric(sb, "Files without a built-in signature match", rows.Count(r => r.NoSignatureMatch).ToString(CultureInfo.InvariantCulture));
