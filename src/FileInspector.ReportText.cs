    {        internal static string BuildReportText(FileReport report)
        {
            var sb = new StringBuilder();
            sb.AppendLine(report.DisplayName);
            sb.AppendLine(new string('=', report.DisplayName.Length));
            foreach (var section in report.Sections)
            {
                sb.AppendLine();
                sb.AppendLine(section.Title);
                sb.AppendLine(new string('-', section.Title.Length));
                foreach (var item in section.Items)
                {
                    var detail = ReportSection.NormalizeDetailText(item.Detail);
                    if (string.Equals((item.Title ?? string.Empty).Trim(), detail.Trim(), StringComparison.Ordinal))
                    {
                        sb.AppendLine(item.Title);
                    }
                    else
                    {
                        sb.AppendLine(item.Title + ":");
                        sb.AppendLine(detail);
                    }
                }
            }
            return sb.ToString().TrimEnd();
        }

        internal static string BuildCombinedText(IEnumerable<FileReport> reports)
        {
            return BuildCombinedText(reports, null);
        }

        internal static string BuildCombinedText(IEnumerable<FileReport> reports, TimeSpan? elapsed)
        {
            var reportList = reports.ToList();
            var sb = new StringBuilder();
            if (reportList.Count > 1)
            {
                AppendTextOverview(sb, reportList, elapsed);
                sb.AppendLine();
            }
            foreach (var report in reportList)
            {
                sb.AppendLine(report.FullText ?? BuildReportText(report));
                sb.AppendLine();
            }
            return sb.ToString().TrimEnd();
        }

        internal static string BuildOverviewText(IEnumerable<FileReport> reports, TimeSpan? elapsed)
        {
            var reportList = reports.ToList();
            if (reportList.Count <= 1)
                return string.Empty;
            var sb = new StringBuilder();
            AppendTextOverview(sb, reportList, elapsed);
            return sb.ToString().TrimEnd();
        }

        internal static string BuildCombinedHtml(IEnumerable<FileReport> reports, string title)
        {
            return BuildCombinedHtml(reports, title, null);
        }

